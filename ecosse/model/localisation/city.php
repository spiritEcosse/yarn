    public function addCity($data) {
        $this->db->query("INSERT INTO " . DB_PREFIX . "city SET name = '" . $this->db->escape($data['name']) . "', zone_id = '" . (int)$data['zone_id'] . "', status = '" . (int)$data['status'] . "', zone_price = '" . (float)$data['zone_price'] . "' ");

